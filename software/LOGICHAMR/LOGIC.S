* FILLBUF - READ SAMPLES FROM FPGA
*-------------------------------
* IN:  Y = CHANNEL NUMBER (0-7)
* OUT: TMPBUF FILLED WITH 38 SAMPLES
* CLOBBERS: A, X
* PRESERVES: Y
*-------------------------------
* FPGA REGISTERS:
*   $C0C0 = CHANNEL SELECT
*   $C0C1 = SAMPLE ADDRESS
*   $C0C2 = DATA OUT
*   $C0C3 = COMMAND (2=READ)
*   $C0C4 = STATUS (BIT0=BUSY)
*   $C0C5 = STRETCH FACTOR
*   $C0C6 = TRIGGER CHANNEL
*   $C0C7 = TRIGGER MODE
*   $C0C8 = WINDOW PRESET
*   $C0C9 = ARM CAPTURE
*-------------------------------

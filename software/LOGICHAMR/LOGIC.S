FILLBUF
 STY $C0C0           ; SELECT CHANNEL
 LDX #$00            ; SAMPLE INDEX
:RDLOOP
 STX $C0C1           ; SET SAMPLE ADDR
 LDA #$02
 STA $C0C3           ; SEND READ CMD
:WAIT
 LDA $C0C4           ; CHECK STATUS
 LSR                 ; BIT0 -> CARRY
 BCS :WAIT           ; LOOP IF BUSY
 LDA $C0C2           ; READ DATA
 STA TMPBUF,X        ; STORE IN BUFFER
 INX
 CPX #$26            ; 38 SAMPLES
 BNE :RDLOOP
 LDA #$02
 STA $C0C4           ; SIGNAL READY
 RTS


